    /**
     * Guardar datos de codigo fuente
     */
    public void guardarDatos() {
        String Text = "";
        try {
            JFileChooser fc = new JFileChooser(System.getProperty("user.dir"));
            fc.showSaveDialog(this);
            File Guardar = fc.getSelectedFile();
            if (Guardar != null) {
                FileWriter Guardx = new FileWriter(Guardar);
                Guardx.write(this.txtCodigoFuente.getText());
                Guardx.close();
            }
        } catch (IOException ioe) {
            System.out.println(ioe);
        }
    }

    /**
     * Cargar archivo txt por openfiledialog
     */
    public void cargarDatos() {
        JFileChooser chooser;
        chooser = new JFileChooser();
        int sel = chooser.showOpenDialog(this.txtCodigoFuente);
        if (sel == chooser.APPROVE_OPTION) {
            File selFile;
            selFile = chooser.getSelectedFile();
            cargarTxt(selFile.getAbsolutePath());
        }
    }

    /**
     * Carga contenido de txt
     *
     * @param xArchivo
     */
    private void cargarTxt(String xArchivo) {
        statusMsg2.setText("abriendo " + xArchivo);
        String linea;
        try {
            FileReader fr = new FileReader(xArchivo);
            BufferedReader entArch = new BufferedReader(fr);
            linea = entArch.readLine();
            txtCodigoFuente.setText("");
            while (linea != null) {
                txtCodigoFuente.setText(txtCodigoFuente.getText() + linea + "\n");
                linea = entArch.readLine();
            }
            entArch.close();
            txtCodigoFuente.setForeground(Color.red);
        } catch (IOException e) {
        }
    }

